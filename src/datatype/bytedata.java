package datatype;

public class bytedata {
public static void main(String[]args) {
	byte b = 127;
	byte a = -128;
	System.out.println("1 byte 8 bits");
	System.out.println(b +" " +a);
	short s = 32767;
	short d = -32766;
	System.out.println("2 byte 16 bits");
	System.out.println(s +" " +d);
	int i = 2147483647;
	int l = -2147483648;
	System.out.println("4 byte 32 bits");
	System.out.println(i +" " +l);
	long L = 34253455l;
	long E = -5466466l;
	System.out.println("8 byte 64 bits");
	System.out.println(L +" " +E);
	
}
  
}
